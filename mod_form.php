defined('MOODLE_INTERNAL') || die;

require_once($CFG->dirroot.'/course/moodleform_mod.php');
require_once($CFG->dirroot.'/mod/cma/locallib.php');
require_once($CFG->libdir.'/filelib.php');

class mod_cma_mod_form extends moodleform_mod {
    public static $datefieldoptions = array('optional' => true, 'step' => 1);

    public function definition() {
        global $CFG;

        $mform = $this->_form;

        $mform->addElement('header', 'general', get_string('general', 'form'));
        $mform->addElement('text', 'name', get_string('name'), array('size' => '48'));
        if (!empty($CFG->formatstringstriptags)) {
            $mform->setType('name', PARAM_TEXT);
        } else {
            $mform->setType('name', PARAM_CLEANHTML);
        }
        $mform->addRule('name', null, 'required', null, 'client');
        $mform->addRule('name', get_string('maximumchars', '', 255), 'maxlength', 255, 'client');
        $this->standard_intro_elements();

        // Open and close dates.
        $mform->addElement('date_time_selector', 'timeopen', get_string('timeopen', 'cma'),
                self::$datefieldoptions);

        $mform->addElement('date_time_selector', 'timeclose', get_string('timeclose', 'cma'),
                self::$datefieldoptions);

        $this->standard_coursemodule_elements();

        $this->add_action_buttons();

        $mform->addElement('hidden', 'revision');
        $mform->setType('revision', PARAM_INT);
        $mform->setDefault('revision', 1);
    }

    public function add_completion_rules() {

        $mform = $this->_form;
        $mform->addElement('checkbox', 'cmacompletion', '', get_string('completionplayed', 'mod_cma'));
        $mform->setDefault('cmacompletion', 1);
        return array('cmacompletion');
    }

    /**
     * Called during validation to see whether some module-specific completion rules are selected.
     *
     * @param array $data Input data not yet validated.
     * @return bool True if one or more rules is enabled, false if none are.
     */
    public function completion_rule_enabled($data) {
        return !empty($data['cmacompletion']);
    }

}

